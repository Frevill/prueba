public class Persona {
    public int id;
    String nombre;
    String apellido;
    int telefono;

    public Persona(){
    }

    public Persona(int id, String nombre, String apellido, int telefono){
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.telefono = telefono;
    }




}
